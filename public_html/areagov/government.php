<p>Parkside Area Government serves as the voice of the residents of the Parkside community. Parkside Area Government provides an opportunity for residents to voice their concerns and suggestions and to get those issues addressed. The area government is composed of fifteen executive board members and twelve floor senators divided evenly between the suites and the apartments. Parkside residents are always welcome at area government meetings to give their input.</p>
<b>Get Involved</b><br>
Are you interested in helping to plan Parkside events and promote a strong sense of community among residents? Area government always welcomes participation by residents on whatever level they feel comfortable. Have a cool idea for a program? Talk with your RA or floor senator. You may even pitch the idea at an area government meeting. If area government likes it, it could get funding and advertising. You can also join any of the committees inside area government to play an active role in planning events.
<br><br>
<b>Committees</b><br>
Parkside Area Government's work is broken down into committees composed of dedicated students working on advocacy and planning events for residents. Check out an excerpt from the 2003-2004 Parkside Scrapbook on the work the committees do for the Parkside community.<br>
<a href="committees.pdf">Committees(pdf)</a>
<br><br>
<b>Highlights from the 2003-2004 Parkside Area Government</b><br>
Parkside Area Government has planned and sponsored a number of programs for Parkside residents including Krazy Karaoke Night, Spirits at Troy, Winter Wonder World, International House of Fashion, and much more.<br>
Every year, Parkside Area Government composes a scrapbook highlighting the year's accomplishments. This year's scrapbook theme was "Parkside International Airways," highlighting the strength and diversity of the Parkside community. Available this year is an online digital version of the scrapbook giving an inside look on Parkside Area Government and what AG has done this year. The online version is heavily compressed for the online environment and does not contain all the content or the quality of the original.<br>
<a href="PircScrapbookLite.pdf">Parkside Area Government Scrapbook 2003-2004 (5.7MB pdf)</a>
