<h3>Resources</h3>
<p>There are many resources available to Parkside residents and USC students in general. In order to assist students in collaboration and to publicize obscure USC online resources, the Parkside website has compiled a listing of available online resources. Please note that these links are provided as a courtesy to Parkside residents and do not in any way imply an endorsement by Parkside Area Government. Parkside Area Government is not responsible for the content in these external sites.</p>
<p>If you have a USC resource that you would like Parkside residents to know about. Email it to <a href="mailto:&#112;ir&#99;&#64;us&#99;&#46;ed&#117;">&#112;ir&#99;&#64;us&#99;&#46;ed&#117;</a></p>
<p><b>MySnapp.com</b> -- <a href="http://www.mysnapp.com" target="_blank">http://www.mysnapp.com</a><br>
A calendar of USC events sponsored by Student Senate.</p>
<p><b>USC Housing</b> -- <a href="http://housing.usc.edu" target="_blank">http://housing.usc.edu</a><br>
Have questions about housing for next semester? Check out USC's housing website.</p>
<p><b>USC Student Directory</b> -- <a href="http://www.usc.edu/directories/student.html" target="_blank">http://www.usc.edu/directories/student.html</a><br>
The directory allows the USC community to look up contact information for fellow students using a variety of criteria. Note that searching the directory is locked to the USC network.</p> 
<p><b>Senate Course Guide</b> -- <a href="http://www.senatecourseguide.com/" target="_blank">http://www.senatecourseguide.com/</a><br>
Sponsored by the USC Student Senate. This site provides student reviews on USC professors.</p>
<p><b>Senate Book Exchange</b> -- <a href="http://www.senatebookexchange.com/" target="_blank">http://www.senatebookexchange.com/</a><br>
Sponsored by the USC Student Senate. This site provides an exchange service to match up USC students for the purpose of buying and selling textbooks.</p>
<p><b>USC Information Services Division</b> -- <a href="http://www.usc.edu/isd/" target="_blank">http://www.usc.edu/isd/</a><br>
USC's Information Services Division site offers technical information for USC students. It includes various tutorials and documentation regarding USC technical resources. In addition, there are various downloads available on ISD's website at <a href="http://www.usc.edu/isd/software/" target="_blank">http://www.usc.edu/isd/software/</a> including programs such as WS FTP, Cisco VPN Client, and Symantec AntiVirus.</p>
